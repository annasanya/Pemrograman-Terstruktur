def luasSegitiga(a,t):
    luas = a*t/2
    return luas

alasA = 10
tinggiA = 20
alasB = 15
tinggiB = 45
print('Luas segitiga dg alas', alasA,
      'dan tinggi', tinggiA,
      'adalah', luasSegitiga(alasA,tinggiA))
print('Luas segitiga dg alas', alasB,
      'dan tinggi', tinggiB,
      'adalah', luasSegitiga(alasB,tinggiB))
